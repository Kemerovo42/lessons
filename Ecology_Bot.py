@bot.message_handler(commands=['help'])
def help_command(message):
    help_text = "/start - приветствие | /help - список команд | /event - случайная экологическая катастрофа"
    bot.send_message(message.chat.id, help_text)

@bot.message_handler(func=lambda message: True)
def all_messages(message):
    if message.text not in ['/start', '/event', '/help']:
        bot.send_message(message.chat.id, "Я понимаю только команды. Напишите /help для списка команд.")
bot.polling()
